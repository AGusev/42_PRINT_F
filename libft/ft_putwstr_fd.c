#include "libft.h"

void	ft_putwstr_fd(wchar_t *s, int fd)
{
	int		i;

	i = 0;
	if (s == NULL)
		return ;
	while (s[i])
	{
		ft_putwchar_fd(s[i], fd);
		++i;
	}
}
